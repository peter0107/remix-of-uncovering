금융 서비스에서는 한도 계산이 정확해야 할 뿐 아니라, 여러 기기에서 동시에 요청할 때도 안전해야 하고, 장애가 발생했을 때 고객과 운영팀이 상황을 이해할 수 있어야 합니다.

## 기능 요청

- 앱 사용자가 송금 화면에 진입하거나 금액을 변경할 때 한도를 조회한다.
- 개인 고객만 대상이며 기업 고객·외화 계좌는 이번 범위에서 제외한다.
