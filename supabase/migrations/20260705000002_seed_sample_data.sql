## 2. 초심자용 배경 설명

### 서비스 기획(PM)이란?

서비스 기획 또는 프로덕트 매니저(PM)는 제품(앱, 웹 서비스)의 기능과 사용자 경험을 설계하고, 출시 후 데이터를 보며 개선하는 역할입니다.

개발자가 "어떻게 만들까"를 고민한다면, PM은 **"무엇을 왜 만들고, 잘 되고 있는지 어떻게 판단할까"**를 고민합니다.

### 구매 전환 퍼널이란?

사용자가 상품을 발견해서 구매를 완료하기까지의 단계를 퍼널(깔때기)이라고 부릅니다. 각 단계마다 일부 유저가 이탈하기 때문에 깔때기 모양이 됩니다.

```
[앱 방문] → [상품 상세페이지 조회] → [장바구니 담기] → [결제 시작] → [결제 완료]
  100%          70%                   12%           9%           8%
```

각 단계 사이의 전환율(%)이 PM이 가장 중요하게 보는 지표입니다.

### PM이 보는 주요 데이터

| 데이터 | 의미 |
| --- | --- |
| DAU (일간 활성 사용자) | 하루에 앱을 사용하는 유저 수 |
| 전환율 | 특정 단계에서 다음 단계로 넘어간 비율 |
| 이탈률 | 특정 단계에서 떠난 비율 |
| 객단가 | 구매 1건당 평균 결제 금액 |
| 세션 시간 | 유저가 앱에 머무는 평균 시간 |
| VOC | 고객의 소리 (CS 문의, 리뷰, 인앱 피드백) |

### 롤백이란?

변경 사항을 되돌려 이전 버전으로 복원하는 것입니다. 문제가 심각할 때 사용하지만, 되돌리는 것 자체에도 비용과 리스크가 따릅니다.

