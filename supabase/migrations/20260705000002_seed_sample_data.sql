#### 1) 현재 상황 요약

**작성 질문**

현재 기획전의 판매 현황과 문제점을 5~7문장으로 요약해주세요.

**포함할 내용**

1. 시즌 전체 소진율이 계획 대비 어느 수준인가
2. 호조/보통/부진/심각으로 상품군이 어떻게 나뉘는가
3. 마진율 실적이 계획 대비 어떤가
4. 시즌 종료까지 잔여 기간은 얼마이고, 무엇이 위험한가
5. 왜 지금 전략을 재수립해야 하는가

> **▼ 초심자용 힌트 1)**
>
> 1. 전체 소진율(32%)과 목표(45%)의 갭을 먼저 확인하세요.
> 2. 시즌 종료 시 85% 소진이 목표인데, 남은 8주 동안 53%p를 더 소진해야 합니다. 현재 속도(주당 약 6.4%p)로 가능한지 계산해보세요.
> 3. 모든 상품이 다 문제가 아닙니다. 호조 상품 3개가 전체를 끌어올리고 있는 구조입니다.

