> 3. 수정 후 재테스트를 권고할 경우: **어떤 변수를 바꿀지, 테스트 기간은 얼마로 할지** 구체적으로 쓰세요.
> 4. "A와 B를 결합"을 권고할 경우: 어떤 요소를 결합하는지 구체화하지 않으면 실행이 안 됩니다.
> 5. 경영진은 분기 목표도 중요하게 봅니다. 재테스트를 권고할 경우 **시간 비용도 고려**했는지 보여주세요.

