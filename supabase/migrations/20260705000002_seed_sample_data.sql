이 플랫폼은 자사몰(웹/앱)을 통해 가구, 인테리어 소품, 홈패브릭, 생활용품 등을 판매하며, 월 거래액은 약 45억원 규모다. 리빙/홈 카테고리는 전체 거래액의 약 35%를 차지하고 있다.

현재 여름 시즌(6~8월) 한정 기획전 **"썸머 리프레시"**가 진행 중이다. 6월 1일에 시작하여 8월 31일에 종료 예정이며, 현재 **7월 첫째 주**(시즌 5주차)에 접어들었다.

기획전을 위해 6개 브랜드로부터 총 12개 상품군, 약 **8억원 규모의 재고**를 매입했다. 그런데 5주차 시점에서 전체 소진율이 **계획 대비 크게 뒤처지는 상품군**이 확인되고 있다.

각 관계자의 입장은 다음과 같다:

- **재무팀:** "마진율이 계획보다 떨어지고 있다. 마크다운을 하면 적자 전환 가능성이 있는 상품군이 있다. 무턱대고 할인하지 말아라."
- **마케팅팀:** "메인 배너와 SNS 광고를 어떤 상품에 집중할지 이번 주 안에 알려줘야 한다. 시즌 후반부 광고비 배분 계획을 확정해야 한다."
- **물류팀:** "시즌 종료 후에도 재고가 남으면 창고비가 월 800만원씩 추가 발생한다. 역시즌 재고는 처분이 매우 어렵다."
- **브랜드 A사 (독점 브랜드):** "할인율 30%가 넘으면 브랜드 이미지 훼손이 우려된다. 차라리 물량을 줄이더라도 정가 판매를 유지해달라."
- **브랜드 D사 (범용 브랜드):** "재고가 남으면 반품 받겠다. 단, 반품 시 물류비는 리브하우스가 부담해야 한다."
- **영업팀장:** "시즌 종료 시점 재고 잔량이 매입액의 15% 이하가 되어야 한다. 이것이 MD 성과 기준이다."

나는 제시되는 데이터를 이용해, 다음을 판단해야 한다.

1. 상품군별 판매 현황과 소진율을 분석하고, 정상 궤도인 상품과 위험 상품을 구분한다.
2. 판매 부진 원인을 가격, 노출, 시즌성, 경쟁 관점에서 분석한다.
3. 상품군별 대응 전략(마크다운/번들/진열 변경/반품/폐기 등)을 수립한다.
4. 마진율과 재고 소진율 사이의 트레이드오프를 판단한다.
5. 시즌 후반부 마케팅 자원 배분을 결정한다.
6. 브랜드사 및 내부 부서와의 협업 요청을 정리한다.

