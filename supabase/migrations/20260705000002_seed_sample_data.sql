### 1. 상황 제시

나는 국내 제약사 임상개발팀 소속 CRA(임상시험 모니터요원)로, 현재 **제2형 당뇨병 치료제 "DM-204"**의 Phase III 다기관 임상시험을 담당하고 있다.

이 시험은 전국 5개 병원(실시기관)에서 동시에 진행 중이며, 총 360명의 피험자가 등록되어 있다. 시험약군(DM-204)에 240명, 대조약군(기존 치료제)에 120명이 배정되어 있다.

최근 4주간 모니터링 보고를 정리하던 중, **특정 실시기관에서 간 수치(ALT) 상승 이상반응이 집중 발생**한 것을 확인했다. 전체적으로도 간 수치 관련 이상반응이 프로토콜 예상 범위보다 높은 빈도로 보고되고 있다.

각 이해관계자의 의견은 다음과 같다:

- **의뢰자(Sponsor) 임상팀장:** "아직 인과관계가 확인된 것은 아니다. 데이터를 좀 더 모아 보자. 이 시험이 지연되면 경쟁사보다 허가가 늦어진다."
- **실시기관 A 시험책임자(PI):** "우리 기관에서 ALT 상승이 3건 연속 나왔다. 피험자 안전이 우선이므로 해당 기관이라도 등록을 일시 중단해야 한다고 본다."
- **데이터안전성모니터링위원회(DSMB):** "다음 정기 검토가 2주 후 예정되어 있다. 중간 검토 소집 여부를 의뢰자가 판단해달라."
- **안전성 담당(PV팀):** "규제기관 보고 기한이 있는 건이 포함될 수 있다. SAE/SUSAR 해당 여부를 빨리 판단해야 한다."
- **규제팀:** "MFDS(식약처) IND 안전성 보고 의무를 놓치면 시험 전체가 중단될 수 있다."

나는 제시되는 데이터를 이용해, 다음을 판단해야 한다.

1. 보고된 이상반응의 심각도와 인과관계를 분류한다.
2. 규제기관 보고 의무(SAE/SUSAR)에 해당하는 건이 있는지 판단한다.
3. 기관별·군별 발생 패턴을 분석해 원인 가설을 세운다.
4. DSMB 긴급 소집 여부를 판단한다.
5. 시험 지속 / 해당 기관 일시 중단 / 전체 일시 중단을 판단한다.
6. 의뢰자, PI, IRB, 규제기관에 대한 커뮤니케이션 계획을 수립한다.

