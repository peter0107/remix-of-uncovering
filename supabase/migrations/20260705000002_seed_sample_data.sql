### ▼ 2. 초심자용 배경 설명

#### 데이터 분석가란?

데이터 분석가는 숫자와 데이터를 보고, **"무슨 일이 일어나고 있는가, 왜 그런가, 어떻게 해야 하는가"**를 찾아 의사결정자에게 알려주는 역할입니다.

직접 캠페인을 기획하거나 마케팅을 집행하지는 않지만, 데이터를 통해 **"이 캠페인이 효과가 있었는가, 계속할 가치가 있는가"**를 판단하는 근거를 만듭니다.

#### A/B 테스트란?

두 가지(또는 그 이상) 방법을 동시에 시험하여, 어느 쪽이 더 나은 결과를 내는지 비교하는 실험입니다.

```
[신규 방문자 30,000명]
  ├── 10,000명 → 캠페인 A (무료 체험 + 할인)
  ├── 10,000명 → 캠페인 B (인기 강의 무료 제공)
  └── 10,000명 → 대조군 (기존 방식)
```

무작위로 배정하기 때문에, 결과의 차이가 캠페인 차이 때문인지 아니면 우연인지를 판단할 수 있습니다.

#### 데이터 분석가가 보는 주요 지표

| 지표 | 의미 |
| --- | --- |
| 전환율 (Conversion Rate) | 방문자 중 유료 결제까지 한 비율 |
| ARPU (Average Revenue Per User) | 유저 1명당 평균 매출 |
| CAC (Customer Acquisition Cost) | 유저 1명을 획득하는 데 든 비용 |
| LTV (Lifetime Value) | 유저 1명이 서비스를 사용하는 동안 가져다주는 총 매출 |
| 잔존율 (Retention Rate) | 일정 기간 후에도 구독을 유지하는 비율 |
| 통계적 유의성 (Statistical Significance) | 결과 차이가 우연이 아니라 실제 차이일 확률. 보통 p < 0.05이면 유의미하다고 판단 |
| 강의 완료율 | 수강을 시작한 강의를 끝까지 완료한 비율. 서비스 만족도와 관련 |

#### "좋은 캠페인"이란?

가입자를 많이 데려오는 것이 항상 좋은 건 아닙니다. 할인으로 들어왔다가 바로 이탈하는 유저가 많으면, **돈은 쓰고 남는 건 없는 캠페인**이 됩니다.

반대로, 가입자는 적지만 오래 남아서 꾸준히 결제하는 유저를 데려오는 캠페인이 장기적으로 더 나을 수 있습니다.

