### 1. 상황 제시

나는 구독형 온라인 교육 플랫폼 **"런잇(LearnIt)"**의 데이터 분석팀 소속 주니어 분석가다.

런잇은 월 구독료(월 19,900원)를 내면 모든 강의를 수강할 수 있는 서비스로, 현재 월간 유료 구독자 약 85,000명을 보유하고 있다.

마케팅팀이 **신규 가입자의 유료 전환율을 높이기 위한 캠페인 2가지**를 기획하여, 지난 4주간 A/B 테스트를 진행했다.

- **캠페인 A: "7일 무료 체험 + 첫 달 50% 할인"** — 가입 허들을 낮추는 전략
- **캠페인 B: "가입 즉시 인기 강의 3개 무료 제공 (체험 없이 정가 결제)"** — 콘텐츠 가치를 어필하는 전략
- **대조군(Control): 기존 방식** — 가입 페이지에서 월 19,900원 정가 결제 안내

테스트 결과가 나왔고, 마케팅팀장이 **내일 경영진 회의에서 캠페인 확대 적용 여부를 발표**해야 한다. 나에게 **데이터 분석 결과와 권고안을 오늘 중으로 보고서로 제출해달라**고 요청했다.

각 부서에서는 다음과 같은 의견을 내고 있다:

- **마케팅팀장:** "캠페인 A가 가입자를 많이 데려오니까 A로 확대 적용하자. 경영진이 '성장' 숫자를 보고 싶어 한다."
- **재무팀:** "캠페인 A는 첫 달 할인 때문에 ARPU(인당 매출)가 낮다. 수익성을 같이 봐야 한다."
- **콘텐츠팀:** "캠페인 B로 들어온 유저가 강의 완료율이 높다. 장기적으로 더 좋은 유저일 수 있다."
- **CRM팀:** "무료 체험 후 결제 전환 없이 이탈하는 유저가 많으면 나중에 재획득 비용이 더 든다."
- **경영진:** "분기 신규 구독자 목표 달성이 중요하지만, 돈만 쓰고 남는 게 없는 캠페인은 안 된다."

나는 제시되는 데이터를 이용해, 다음을 판단해야 한다.

1. 캠페인 A, B, 대조군의 성과를 정확히 비교한다.
2. "좋은 캠페인"의 기준이 무엇인지 정의한다.
3. 단기 지표(가입, 전환)와 중장기 지표(잔존, 수익성)를 구분해 분석한다.
4. 결과가 통계적으로 신뢰할 수 있는지 판단한다.
5. 캠페인 확대 / 수정 후 재테스트 / 중단 중 하나를 권고한다.
6. 권고안의 근거와 한계를 함께 제시한다.

