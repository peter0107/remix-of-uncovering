### 2. 초심자용 배경 설명

#### 임상시험이란?

새로운 약이 사람에게 안전하고 효과가 있는지 확인하기 위해, 실제 환자를 대상으로 체계적으로 진행하는 시험입니다.

단계별로 보면:

- **Phase I:** 소수의 건강한 사람 대상, 안전한 용량 범위를 확인
- **Phase II:** 소규모 환자 대상, 효과가 있는지 탐색
- **Phase III:** 대규모 환자 대상, 기존 치료제와 비교해서 효과와 안전성을 최종 확인 ← **이 시뮬레이션의 단계**
- **Phase IV:** 허가 후 시판 상황에서의 추가 안전성 모니터링

#### CRA(임상시험 모니터요원)란?

CRA는 제약사(의뢰자) 측에서 실시기관(병원)을 방문하여 임상시험이 프로토콜대로 진행되고 있는지 확인하고, 피험자 안전 관련 사항을 모니터링하는 역할입니다.

쉽게 말해, **"시험이 규정대로 진행되고 있는가, 피험자에게 위험한 일이 생기지 않았는가"**를 현장에서 확인하는 사람입니다.

#### 주요 용어 정리

| 용어 | 의미 |
| --- | --- |
| 이상반응 (AE, Adverse Event) | 임상시험 중 피험자에게 발생한 바람직하지 않은 의학적 사건. 시험약과의 인과관계와 무관하게 모두 기록함 |
| 중대한 이상반응 (SAE, Serious Adverse Event) | 사망, 입원, 생명 위협, 영구적 장애 등 심각한 결과를 초래하는 이상반응 |
| SUSAR (Suspected Unexpected Serious Adverse Reaction) | 시험약과 인과관계가 의심되며, 예상하지 못했던 중대한 이상반응. **규제기관에 신속 보고 의무** |
| ALT (간 효소 수치) | 간세포 손상 시 혈액에서 상승하는 효소. 정상 범위 상한(ULN)의 3배 이상이면 주의, 5배 이상이면 중단 고려 |
| DSMB (데이터안전성모니터링위원회) | 임상시험 중 안전성 데이터를 독립적으로 검토하는 외부 전문가 위원회 |
| IRB (임상시험심사위원회) | 각 병원에서 피험자 보호를 위해 임상시험을 심사·감독하는 위원회 |
| PI (시험책임자) | 각 실시기관에서 임상시험을 책임지는 의사 |
| 프로토콜 | 임상시험 설계서. 시험 방법, 대상, 평가 기준, 안전성 관리 기준 등을 규정한 문서 |

#### CRA가 보는 주요 데이터

| 데이터 | 의미 |
| --- | --- |
| AE/SAE 발생 건수 | 이상반응이 얼마나, 어디서 발생했는지 |
| 발생률(시험약군 vs 대조군) | 시험약이 원인일 가능성을 판단하는 근거 |
| 기관별 발생 패턴 | 특정 기관에 문제가 집중되는지 |
| 시간 경과에 따른 추이 | 특정 시점부터 증가했는지, 점진적인지 |
| 인과관계 평가 | 시험약과 관련 있는지, 다른 원인인지 |

