#### 2. 초심자용 배경 설명

#### 외부감사란?

외부감사는 회사 바깥의 독립적인 회계법인이 기업의 재무제표가 정확하고 믿을 수 있는지 검증하는 절차입니다.

쉽게 말해, **"이 회사가 발표한 매출, 비용, 이익 숫자가 진짜 맞는가?"**를 확인하는 일입니다.

#### 매출 인식 시점이 왜 중요한가?

매출은 **상품이 고객에게 인도되고, 대가를 받을 권리가 확정된 시점**에 기록해야 합니다. 아직 물건을 보내지 않았는데 미리 매출로 잡으면 실제보다 실적이 부풀려 보이게 됩니다.

12월 결산 기업에서 "12월 31일까지 매출을 더 잡고 싶다"는 유인이 생길 수 있고, 이것이 감사에서 가장 흔하게 검토하는 위험 영역 중 하나입니다.

#### 중요성(Materiality)이란?

감사에서는 모든 오류를 찾는 것이 아니라, **재무제표 이용자의 판단에 영향을 줄 수 있는 수준의 오류**를 찾습니다. 이 기준 금액을 중요성(Materiality)이라고 합니다.

예를 들어 연매출 500억 회사에서 10만원 기록 오류는 중요하지 않지만, 15억원 매출을 잘못 기록했다면 투자자 판단에 영향을 줄 수 있습니다.

#### 감사팀이 보는 주요 데이터와 의미

| 데이터 | 의미 |
| --- | --- |
| 매출 인식 시점 vs 인도 시점 | 매출을 기록한 날짜와 실제 상품이 전달된 날짜가 일치하는지 |
| 중요성 금액 | 오류 합계가 이 금액을 넘으면 재무제표 전체의 신뢰성 문제로 이어질 수 있음 |
| 표본 오류율 | 검증한 표본 중 문제가 있는 비율. 높으면 전체에도 문제가 있을 가능성 시사 |
| 증빙 서류 | 세금계산서, 거래명세서, 배송 완료 확인서, 고객 검수 확인서 등 |
| 전기 대비 추이 | 전년도와 비교해 특이한 변동이 있는지 확인 |

