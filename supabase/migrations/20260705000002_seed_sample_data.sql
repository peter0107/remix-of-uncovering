### 1. 상황 제시

나는 전기차 전문 완성차 업체 **"EV모터스"**의 양산 품질팀 소속 주니어 품질 엔지니어다.

현재 주력 모델 **"EV-3"**의 배터리 모듈 조립 라인을 담당하고 있다. EV-3는 월 5,000대 생산 중이며, 양산 시작 6개월 차다.

최근 2주간 **배터리 모듈 조립 공정에서 두 가지 품질 이슈**가 동시에 발생하고 있다.

**이슈 1: 볼트 체결 토크값 이상**

배터리 모듈을 차체에 고정하는 M10 볼트 16개 중 일부에서 토크값이 규격 하한에 근접하거나 미달하는 건이 발생하고 있다.

**이슈 2: 방수 시험 누수**

배터리 모듈 조립 완료 후 진행하는 방수 시험(IP67 기준)에서 누수 불합격이 증가하고 있다.

두 이슈가 관련이 있는지, 독립적인 문제인지 아직 확인되지 않았다.

각 부서에서는 다음과 같은 의견을 내고 있다:

- **생산팀:** "라인을 멈추면 이번 달 생산 목표(5,000대)를 못 맞춘다. 출하 전 전수검사를 강화하면 되지 않나?"
- **구매팀(SQE):** "볼트 공급업체에서 최근 Lot에 치수 편차가 있을 수 있다. 확인 중이지만 시간이 걸린다."
- **설비팀:** "토크 렌치 캘리브레이션은 정상이었다. 다만 2번 라인 체결 로봇의 진동값이 최근 약간 올랐다."
- **고객품질팀:** "아직 필드 클레임은 없지만, 배터리 누수는 안전 직결 이슈다. 이미 출하된 차량 중 해당 기간 생산분도 확인해야 한다."
- **경영진:** "리콜 사태가 나면 브랜드에 치명적이다. 하지만 생산 중단도 딜러 재고 부족으로 이어진다."

나는 제시되는 데이터를 이용해, 다음을 판단해야 한다.

1. 두 이슈의 발생 패턴을 분석하고, 관련성 여부를 판단한다.
2. 라인별, 시프트별, 공급 Lot별 원인 가설을 세운다.
3. 라인 정지 / 제한 가동 / 전수검사 강화 중 대응을 판단한다.
4. 이미 출하된 차량에 대한 대응 범위를 결정한다.
5. 유관부서 및 공급업체에 필요한 확인과 조치를 요청한다.
6. 재발 방지 대책을 수립한다.

