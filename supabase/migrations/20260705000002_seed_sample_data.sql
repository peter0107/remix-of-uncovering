#### 2) 상품군별 진단 및 분류

**작성 질문**

10개 상품군을 판매 상태에 따라 분류하고, 각 상품군의 핵심 문제를 한 줄로 진단해주세요.

**작성 양식**

| 분류 | 상품군 | 핵심 진단 | 근거 데이터 |
| --- | --- | --- | --- |
| 호조 (추가 투자 검토) |  |  |  |
| 보통 (현행 유지) |  |  |  |
| 부진 (전략 조정 필요) |  |  |  |
| 심각 (긴급 처분 검토) |  |  |  |

> **▼ 초심자용 힌트 2)**
>
> 단순히 소진율만 보지 말고, **왜 안 팔리는지** 원인을 함께 써야 합니다.
>
> 1. **페이지뷰 높음 + 전환율 낮음** → 고객이 보긴 하는데 안 산다 → 가격 또는 상품 자체 문제
> 2. **페이지뷰 낮음 + 전환율 낮음** → 아예 노출이 안 되고 있다 → 진열/마케팅 문제
> 3. **페이지뷰 높음 + 전환율 높음** → 잘 팔리고 있다 → 추가 노출 시 더 성장 가능
> 4. 마진율도 함께 보세요. 잘 팔리더라도 마진이 낮으면 수익 기여가 적습니다.

