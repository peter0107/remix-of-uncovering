### ▼ 2. 초심자용 배경 설명

#### MD(머천다이저)란?

MD는 **어떤 상품을, 얼마나 사서, 얼마에, 어떻게 팔 것인가**를 결정하는 역할입니다.

쉽게 말해:

- 시즌 전: 어떤 상품이 잘 팔릴지 예측하고 → 브랜드/공급사에서 매입
- 시즌 중: 판매 데이터를 보며 → 가격 조정, 노출 변경, 재고 관리
- 시즌 후: 남은 재고를 어떻게 처리할지 → 추가 할인, 반품, 아울렛 전환

#### 주요 개념 정리

| 용어 | 의미 |
| --- | --- |
| 매입가(원가) | 브랜드/공급사에서 상품을 사들인 가격 |
| 판매가(정가) | 소비자에게 판매하는 가격 |
| 마크다운 | 정가에서 할인하여 판매하는 것. 마크다운이 깊을수록 마진이 줄어듦 |
| 마진율 | (판매가 - 매입가) ÷ 판매가. 마진율이 높을수록 수익성이 좋음 |
| 소진율 | 매입한 수량 대비 판매된 수량의 비율. 소진율이 낮으면 재고가 쌓이고 있다는 의미 |
| 재고 회전일수 | 현재 재고가 모두 팔리려면 며칠이 걸리는지. 짧을수록 잘 팔리고 있음 |
| 역시즌 재고 | 시즌이 끝난 후 남은 재고. 처분이 어렵고 가치가 급격히 떨어짐 |
| 번들 | 여러 상품을 묶어서 세트로 판매하는 방식 |

#### MD가 보는 주요 데이터

| 데이터 | 의미 |
| --- | --- |
| 소진율 (계획 vs 실적) | 예상만큼 팔리고 있는지 |
| 마진율 (계획 vs 실적) | 수익성이 유지되고 있는지 |
| 재고 금액 | 팔리지 않은 상품의 원가 합계 |
| 상품별 페이지뷰 / 전환율 | 고객이 보기는 하는데 안 사는지, 아예 안 보는지 |
| 경쟁사 가격 | 같은 상품이나 유사 상품을 경쟁사에서 얼마에 파는지 |

