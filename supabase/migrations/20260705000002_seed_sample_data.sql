## 1. 상황 제시

나는 월 거래액 200억 규모의 커머스 앱에서 **구매 전환 퍼널**을 담당하는 PM이다.

2주 전, 오랜 기간 준비한 **상품 상세페이지 + 구매 플로우 개편**(v2.0)을 전체 유저에 배포했다.

주요 변경 사항은 다음과 같다:

- 상품 상세페이지 UI 전면 개편 (이미지 영역 확대, 상품 정보 탭 구조 변경)
- 구매 버튼 위치 변경 (하단 고정 → 스크롤 연동 플로팅)
- 옵션 선택 방식 변경 (별도 페이지 → 바텀시트)
- 결제 페이지 간소화 (기존 3단계 → 2단계)

출시 1주차 데이터를 확인한 결과, **전체 구매전환율이 하락**한 것을 발견했다. 그런데 일부 지표는 오히려 개선된 부분도 있다.

오늘 오후 팀장에게 **1차 분석 결과와 대응 방향**을 보고해야 한다.

각 부서에서는 다음과 같은 의견을 내고 있다:

- **개발팀:** "롤백하려면 지금 해야 한다. 다음 스프린트에서 v2.0 기반 신규 기능 개발이 이미 시작됐다. 늦어지면 롤백 비용이 급격히 커진다."
- **마케팅팀:** "이번 주 대규모 프로모션이 진행 중이다. 지금 롤백하면 프로모션 랜딩 페이지와 상세페이지 연결이 깨질 수 있다."
- **CS팀:** "출시 이후 '구매 버튼을 못 찾겠다', '할인 정보가 안 보인다'는 문의가 평소 대비 3배 늘었다."
- **디자인팀:** "새 UI는 장기적으로 더 나은 구조다. 기존 유저의 적응 기간이 필요한 것이고, 신규 유저 데이터를 봐야 한다."
- **경영진:** "전환율 회복이 최우선이다. 다만, 단기 수치 반등을 위해 장기 방향성을 포기하지는 않았으면 한다."

나는 제시되는 데이터를 이용해, 다음을 판단해야 한다.

1. 어떤 지표가 실제로 나빠졌고, 어떤 지표는 개선되었는지 구분한다.
2. 퍼널 어느 구간에서 문제가 발생했는지 파악한다.
3. 유저 세그먼트별로 영향이 다른지 확인한다.
4. VOC 유형을 분석해 정성적 원인을 파악한다.
5. 대응안(롤백 / 핫픽스 / 유지+모니터링 / 부분 롤백)의 장단점을 비교한다.
6. 최종 대응안을 선택하고 근거를 작성한다.
7. 유관부서에 필요한 협업 요청을 정리한다.
8. 향후 모니터링 계획과 재발 방지안을 작성한다.

