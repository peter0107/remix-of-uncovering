### ▼ 2. 초심자용 배경 설명

#### 자동차 품질 엔지니어란?

자동차 품질 엔지니어는 양산 과정에서 **"만들어진 차가 설계대로 안전하고 정확하게 만들어졌는가"**를 확인하고, 문제가 발생하면 원인을 분석해 대응하는 역할입니다.

제조 현장에서 불량이 나면 "왜 발생했는가 → 얼마나 심각한가 → 어떻게 대응할 것인가 → 재발을 어떻게 막을 것인가"를 체계적으로 판단합니다.

#### 핵심 용어 정리

| 용어 | 의미 |
| --- | --- |
| 토크 (Torque) | 볼트를 조이는 힘. 규격 범위 내로 조여야 풀리지도 않고 과도하게 조이지도 않음 |
| IP67 | 방수/방진 등급. "6"은 완전 방진, "7"은 수심 1m에서 30분간 침수에도 내부 침수 없음 |
| ppm (Parts Per Million) | 불량률 단위. 100만 개 중 몇 개가 불량인지. 자동차 업계 목표는 보통 50ppm 이하 |
| Cpk (공정능력지수) | 공정이 규격 내에서 얼마나 안정적으로 생산하는지를 나타내는 지수. 1.33 이상이면 양호, 1.0 미만이면 관리 필요 |
| 8D Report | 문제 해결을 위한 8단계 보고서. 자동차 업계 표준 불량 대응 양식 |
| FMEA (고장모드영향분석) | 어떤 부분이 어떻게 고장날 수 있고, 그 영향이 얼마나 심각한지를 사전에 분석하는 기법 |
| 전수검사 | 생산된 모든 제품을 하나하나 검사하는 것. 비용이 높지만 유출을 막을 수 있음 |
| 발출검사 (샘플링 검사) | 일부만 뽑아서 검사하는 것. 효율적이지만 불량이 빠져나갈 수 있음 |
| 리콜 | 이미 고객에게 전달된 차량을 회수하여 수리하는 것. 비용과 브랜드 타격이 매우 큼 |

#### 배터리 모듈 조립이 왜 중요한가?

전기차에서 배터리는 차량 하부에 장착됩니다. 볼트 체결이 부실하면 주행 중 진동으로 모듈이 느슨해질 수 있고, 방수가 되지 않으면 빗물이나 세차수가 배터리에 침투해 합선, 화재 등 심각한 안전사고로 이어질 수 있습니다.

```
[배터리 모듈 조립 흐름]

모듈 입고 → 차체 위치 정렬 → 볼트 체결 (16개소, 로봇 자동)
→ 체결 토크 검증 → 실링 도포 → 방수 시험 (IP67) → 합격 판정 → 다음 공정
```

#### 품질 엔지니어가 보는 주요 데이터

| 데이터 | 의미 |
| --- | --- |
| 토크값 분포 | 볼트 체결력이 규격 안에 안정적으로 들어오는지 |
| Cpk | 공정이 규격 중심에서 얼마나 안정적인지 |
| 불량률(ppm) | 전체 생산 대비 불량 비율 |
| 라인별/시프트별 발생 현황 | 특정 라인이나 작업 시간대에 문제가 집중되는지 |
| 공급업체 입고 검사 데이터 | 부품이 규격대로 납품되고 있는지 |
| 필드 클레임 데이터 | 고객이 사용 중 보고한 불량 |

